package org.spring2023.domain;

/**
 * Enum видов сложностей тренировок
 */
public enum Difficulty {
    /**
     * Элементарный уровень сложности
     */
    ELEMENTARY,
    /**
     * Средний уровень сложности
     */
    MIDDLE,
    /**
     * Продвинутый уровень сложности
     */
    ADVANCED,
    /**
     * Сложный уровень сложности
     */
    HARD,
    /**
     * Экспертный уровень сложности
     */
    EXPERT
}
